function z = twoModeSurrogate(X)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% INPUT:
% X: X=[x1 x2 ... x10]
%
% OUTPUT:
% z:: evaluation surrogate(x1,x2,...,x10)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

z = 0;
for i=0:4
	z = z + twoModeSurrogate2dim([X(2*i+1) X(2*i+2)]);
end

end
